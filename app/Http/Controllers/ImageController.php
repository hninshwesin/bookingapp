    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Image  $image
     * @return \Illuminate\Http\Response
     */
    public function destroy(Image $cover_image)
    {
        $cover_image->delete();

        return redirect()->route('cover_image.index')->with('success', 'Image deleted successfully');
    }
