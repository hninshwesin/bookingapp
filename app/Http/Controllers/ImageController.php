<?php

namespace App\Http\Controllers;

use App\Image;
use Illuminate\Http\Request;

class ImageController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $images = Image::all();

        return view('cover_image.index', compact('images'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('cover_image.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([

            'images' => 'required'

        ]);

        $images = $request->hasFile('images');

        if ($images) {
            $image_files = $request->file('images');
            foreach ($image_files as $image_file) {
                $file = $image_file->store('public/cover_image');

                Image::create([
                    'cover_image' => $file
                ]);
            }
        }

        return redirect()->route('cover_image.index')->with('success', 'Image created successfully.');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Image  $image
     * @return \Illuminate\Http\Response
     */
    public function show(Image $cover_image)
    {
        return view('cover_image.show', compact('cover_image'));
    }

